# Operadores

# Operadores normales
print(3 + 3)
print(3 - 3)
print(3 * 3)
print(3 / 3)
print(10 % 3) # operador de modulo, (saber si hay multiplo)
print(10 // 3) # flor division, lo va a aproximar a un numero entero
print(2 ** 3) # 2 a la 3


print('Hola ' + 'Python!')
print("Hola numero " + str(5) + '!')
print('Hola' * 5 )

# Operadores comparativas

print(3 > 4) # sale false
print(3 < 4) # sale true
print(3 >= 4) # sale false, esta comprobando con una ordenacion alfabetica
print(4 <= 4) # sale True
print(4 == 4) # sale True
print(2 != 4) # sale True
print(3 > 4 == 2) # sale False

# Operadores logicos

print(3 > 4 and "Hola" > "Python")
print(3 > 4 or "Hola" > "Python")
